package onight.tfw.outils.serialize;

public class DesException extends Exception {

	public DesException() {
		super();
	}

	public DesException(String message, Throwable cause) {
		super(message, cause);
	}

	public DesException(String message) {
		super(message);
	}

	public DesException(Throwable cause) {
		super(cause);
	}

	private static final long serialVersionUID = 8555104416199406985L;
}
